#!/usr/bin/env node
// Generates one mp3 voiceover per article via OpenAI TTS, saves to
// assets/audio/articles/<id>.mp3, and regenerates the
// src/lib/audio/article-audio.ts registry. Idempotent — skips files that
// already exist. Run with: `node scripts/generate-article-audio.mjs`.
//
// Requires OPENAI_API_KEY in .env (or exported in the shell).
// Optional overrides: OPENAI_TTS_VOICE (default: shimmer),
//                     OPENAI_TTS_MODEL (default: tts-1).

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const ARTICLES_TS = path.join(ROOT, 'src/lib/resources-data.ts');
const OUT_DIR = path.join(ROOT, 'assets/audio/articles');
const REGISTRY = path.join(ROOT, 'src/lib/audio/article-audio.ts');

const TTS_MAX_CHARS = 4096; // OpenAI limit per call
const REQUEST_GAP_MS = 600; // be polite under default rate limit

// ─── Tiny .env loader so the script works without dotenv ─────────────────────
async function loadEnv() {
  try {
    const raw = await fs.readFile(path.join(ROOT, '.env'), 'utf8');
    for (const line of raw.split('\n')) {
      const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
      if (m && !process.env[m[1]]) {
        // Strip surrounding quotes if present
        process.env[m[1]] = m[2].replace(/^['"]|['"]$/g, '');
      }
    }
  } catch {
    // .env not present — fine, may be set via export
  }
}

await loadEnv();

const API_KEY = process.env.OPENAI_API_KEY;
if (!API_KEY) {
  console.error('Missing OPENAI_API_KEY (add it to .env or export it).');
  process.exit(1);
}

const VOICE = process.env.OPENAI_TTS_VOICE ?? 'shimmer';
const MODEL = process.env.OPENAI_TTS_MODEL ?? 'tts-1';

// ─── Parse articles out of resources-data.ts ─────────────────────────────────
// RESOURCE_ARTICLES is a stable shape: `{ id: '…', category: '…', minutes: N,
// title: '…', excerpt: '…', body: \`…\` }`. Single-quoted strings escape
// internal apostrophes; the body is always a backtick template literal. The
// regex below matches that shape and is robust enough for the controlled
// input — if the format changes, this needs to change.
async function loadArticles() {
  const src = await fs.readFile(ARTICLES_TS, 'utf8');
  const re = /\{\s*id:\s*'([^']+)'[^]*?title:\s*'((?:\\'|[^'])+)'[^]*?body:\s*`([^]*?)`\s*\}/g;
  const articles = [];
  let m;
  while ((m = re.exec(src))) {
    articles.push({
      id: m[1],
      title: m[2].replace(/\\'/g, "'"),
      body: m[3],
    });
  }
  return articles;
}

// Compose the spoken script: title with a sentence pause, then body.
// Truncate to the model's max to stay within a single call.
function buildScript(article) {
  return `${article.title}.\n\n${article.body}`.slice(0, TTS_MAX_CHARS);
}

async function fetchTts(text) {
  const res = await fetch('https://api.openai.com/v1/audio/speech', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${API_KEY}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      model: MODEL,
      voice: VOICE,
      input: text,
      response_format: 'mp3',
    }),
  });
  if (!res.ok) {
    const detail = await res.text();
    throw new Error(`OpenAI TTS ${res.status}: ${detail}`);
  }
  return Buffer.from(await res.arrayBuffer());
}

async function writeRegistry(generatedIds) {
  const sorted = [...generatedIds].sort();
  const lines = [
    '// AUTO-GENERATED by scripts/generate-article-audio.mjs — do not edit by hand.',
    "import type { AudioSource } from 'expo-audio';",
    '',
    'export const ARTICLE_AUDIO: Record<string, AudioSource> = {',
    ...sorted.map((id) => `  '${id}': require('../../../assets/audio/articles/${id}.mp3'),`),
    '};',
    '',
    'export function getArticleAudio(id: string): AudioSource | null {',
    '  return ARTICLE_AUDIO[id] ?? null;',
    '}',
    '',
    'export const HAS_ARTICLE_AUDIO: boolean = Object.keys(ARTICLE_AUDIO).length > 0;',
    '',
  ];
  await fs.mkdir(path.dirname(REGISTRY), { recursive: true });
  await fs.writeFile(REGISTRY, lines.join('\n'), 'utf8');
}

async function main() {
  await fs.mkdir(OUT_DIR, { recursive: true });
  const articles = await loadArticles();
  console.log(`Found ${articles.length} articles. Voice: ${VOICE}, model: ${MODEL}.`);

  const existing = new Set();
  for (const a of articles) {
    try {
      await fs.access(path.join(OUT_DIR, `${a.id}.mp3`));
      existing.add(a.id);
    } catch {}
  }
  console.log(`Already generated: ${existing.size}.`);

  const have = new Set(existing);
  await writeRegistry(have); // ensure registry exists even before new work

  for (let i = 0; i < articles.length; i++) {
    const a = articles[i];
    const out = path.join(OUT_DIR, `${a.id}.mp3`);
    const tag = `[${i + 1}/${articles.length}]`;
    if (existing.has(a.id)) {
      console.log(`${tag} skip ${a.id}`);
      continue;
    }
    const text = buildScript(a);
    console.log(`${tag} generate ${a.id} (${text.length} chars)`);
    try {
      const audio = await fetchTts(text);
      await fs.writeFile(out, audio);
      have.add(a.id);
      await writeRegistry(have);
      await new Promise((r) => setTimeout(r, REQUEST_GAP_MS));
    } catch (err) {
      console.error(`  -- failed ${a.id}: ${err.message}`);
    }
  }

  console.log(`Done. ${have.size}/${articles.length} files in ${OUT_DIR}.`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
